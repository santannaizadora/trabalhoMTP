    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            GerenciadorVendas gv = new GerenciadorVendas();
            GerenciadorItens gi = new GerenciadorItens();
            VerifyInput vi = new VerifyInput();
            ItemVenda iv = new ItemVenda();
            String codigo = vi.ifExists(JOptionPane.showInputDialog("Informe o cÃ³digo do produto"));
            int quant = Integer.parseInt(vi.ifExists((JOptionPane.showInputDialog("Qual a quantidade?"))));
            gi.Produto(codigo);
            iv.setQuantidade(quant);
            iv.salvarItens();
            double valorTotal = Double.parseDouble(jTextField2.getText()) + gv.ValorTotal(codigo, quant);
            jTextField2.setText("");
            jTextField2.setText(String.valueOf(valorTotal));
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null,"Erro");
        }
        preencheTabela();
       
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     try {
           Writer out = new FileWriter("itemVenda.txt");
           out.write("");
           out.flush();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao limpar dados da consulta");
        }
        
        Vendas v = new Vendas();
       VerifyInput vi = new VerifyInput(); 
       try{
           
            v.setCodigo(vi.ifExists(jTextField1.getText()));
            v.setDataVenda(Vendas.formato.parse(vi.ifExists(jFormattedTextField1.getText())));
            v.setValorTotal(Double.parseDouble(vi.ifExists(jTextField2.getText())));
            v.salvarVendas();
            JOptionPane.showMessageDialog(null,"Venda registrada");
       } catch(NumberFormatException | IOException ex){
           JOptionPane.showMessageDialog(null,"Erro ao cadastrar. Verifique os campos preenchidos");
       } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null,"Erro");
        }
       jTextField1.setText("");
       jTextField2.setText("0");
       jFormattedTextField1.setText("");
       preencheTabela(); 
    }//GEN-LAST:event_jButton1ActionPerformed

